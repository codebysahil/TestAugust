package javaSessions;

public class StringConcat {

	public static void main(String[] args) {
	
		int x = 100;
		int y = 200;
		System.out.println(x + y); // addition of the numbers in case of int values
		
		String a = "Hello";
		String b = "Sahil";
		System.out.println(a + b); // concatenation in case of the Strings
		System.out.println(x+b); // this is also concatenation as strings can be concatenated with any data value
				
		

	}

}
